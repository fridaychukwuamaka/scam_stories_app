class Success {
  final int? code;
  final Object? response;
  final String? msg;
  Success({
    this.code,
    this.response,
    this.msg,
  });
}

class Failure {
  final int? code;
  final Object? response;
  final String? msg;
  Failure({
    this.code,
    this.response,
    this.msg,
  });
}
